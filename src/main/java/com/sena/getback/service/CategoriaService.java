package com.sena.getback.service;

import com.sena.getback.model.Categoria;
import com.sena.getback.repository.CategoriaRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.List;
import java.util.Optional;

@Service
public class CategoriaService {

    @Autowired
    private CategoriaRepository categoriaRepository;

    // ðŸ”¹ Listar todas las categorÃ­as
    public List<Categoria> findAll() {
        return categoriaRepository.findAll();
    }

    // ðŸ”¹ Alias para compatibilidad con diferentes nombres
    public List<Categoria> listar() {
        return findAll();
    }

    // ðŸ”¹ Buscar categorÃ­a por id
    public Categoria findById(Integer id) {
        return categoriaRepository.findById(id).orElse(null);
    }

    // ðŸ”¹ Buscar categorÃ­a por id retornando Optional
    public Optional<Categoria> findByIdOptional(Integer id) {
        return categoriaRepository.findById(id);
    }

    // ðŸ”¹ Guardar o actualizar categorÃ­a
    public Categoria save(Categoria categoria) {
        return categoriaRepository.save(categoria);
    }

    // ðŸ”¹ Eliminar categorÃ­a por id
    public void delete(Integer id) {
        categoriaRepository.deleteById(id);
    }

    // ðŸ”¹ Verificar si existe categorÃ­a por id
    public boolean existsById(Integer id) {
        return categoriaRepository.existsById(id);
    }

    // ðŸ”¹ Buscar categorÃ­a por nombre
    public Categoria findByNombre(String nombre) {
        return categoriaRepository.findAll().stream()
                .filter(categoria -> nombre.equalsIgnoreCase(categoria.getNombre()))
                .findFirst()
                .orElse(null);
    }

    // ðŸ”¹ Contar total de categorÃ­as
    public long count() {
        return categoriaRepository.count();
    }
}