package com.sena.getback.service;

import com.sena.getback.model.Menu;
import com.sena.getback.repository.MenuRepository;
import org.springframework.stereotype.Service;

import java.util.List;

@Service
public class MenuService {

    private final MenuRepository menuRepository;

    // InyecciÃ³n por constructor
    public MenuService(MenuRepository menuRepository) {
        this.menuRepository = menuRepository;
    }

    // ðŸ”¹ Listar todos los productos
    public List<Menu> findAll() {
        return menuRepository.findAll();
    }

    // ðŸ”¹ Buscar producto por id (devuelve Menu o null, no Optional)
    public Menu findById(Long id) {
        return menuRepository.findById(id).orElse(null);
    }

    // ðŸ”¹ Guardar o editar producto
    public Menu save(Menu producto) {
        return menuRepository.save(producto);
    }

    // ðŸ”¹ Eliminar producto por id
    public void delete(Long id) {
        menuRepository.deleteById(id);
    }

    // ðŸ”¹ Verificar si un producto existe por id (opcional, pero Ãºtil)
    public boolean existsById(Long id) {
        return menuRepository.existsById(id);
    }
}
